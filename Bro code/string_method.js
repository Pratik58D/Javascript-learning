//string method = allow us to manipulate and work with text(strings)


let userName = " Pratika ";
console.log(userName.charAt(0))   //p

console.log(userName.indexOf("a"))  //2


console.log(userName.lastIndexOf("a"))  //6

console.log(userName.length)  //6

console.log(userName.trim())    //Pratrika
console.log(userName.toUpperCase())  //PRATIKA
console.log(userName.toLowerCase())  //pratika
console.log(userName.repeat(2))     //pratika pratika
console.log(userName.split("t"))     //split on t

